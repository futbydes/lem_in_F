char	*ft_strrchr(const char *s, int c)
{
	char	*x;

	x = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			x = (char*)s;
		s++;
	}
	if (*s == '\0' && (char)c == '\0')
	{
		x = (char*)s;
		return (x);
	}
	else
		return (x);
}
