char	*ft_strcat(char *s1, const char *s2)
{
	int		x;

	x = 0;
	while (s1[x] != '\0')
		x++;
	while (*s2 != '\0')
		s1[x++] = *s2++;
	s1[x] = '\0';
	return (s1);
}
