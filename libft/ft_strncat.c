#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	c;
	int		x;

	c = 0;
	x = 0;
	while (s1[x] != '\0')
		x++;
	while ((c != n) && (*s2 != '\0'))
	{
		s1[x++] = *s2++;
		c++;
	}
	s1[x] = '\0';
	return (s1);
}
