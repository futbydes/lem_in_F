#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		y;
	int		x;
	char	*s2;

	y = 0;
	x = ft_strlen(s1);
	s2 = (char*)malloc(sizeof(char) * x + 1);
	if (s2 == 0)
		return (0);
	while (s1[y] != '\0')
	{
		s2[y] = s1[y];
		y++;
	}
	s2[y] = '\0';
	return (s2);
}
